/// segment that Claude Code's skill discovery looks for).
pub const SKILL_NAME: &str = "gwt-coordination";

/// Skill description surfaced in the auto-discovery listing. Designed to
/// trigger proactive invocation at the start of every conversation in a
/// gwt-managed worktree, independent of project documentation.
pub const SKILL_DESCRIPTION: &str = "Use proactively at the start of every conversation in any gwt-managed worktree, regardless of project AGENTS.md / CLAUDE.md content. Establishes Board posting (gwtd board post) and Work update (gwtd workspace update) rules for coordinating with other agents and the user. Mandatory when posting milestones, claiming scope, or updating current focus.";

