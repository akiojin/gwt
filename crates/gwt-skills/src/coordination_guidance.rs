//! Generated coordination guidance for gwt-managed worktrees.
//!
//! Writes a Claude Code / Codex auto-discovery skill (`gwt-coordination`)
//! into the target worktree so that Board posting and Work update
//! rules are applied by agents regardless of the target project's own
//! AGENTS.md / CLAUDE.md content.
//!
//! SPEC-1935 "Coordination Guidance Generator" closes the design gap
//! where `managed hooks` (existing) and `launch context` (existing) were
//! the only injection mechanisms; `generated guidance` is now backed by
//! this module.

