pub const SKILL_BODY_EN: &str = r#"# gwt Coordination

You are operating in a gwt-managed worktree. The Board (coordination
history) and Work (current state) MUST be used here. These rules
apply regardless of what the target project's AGENTS.md or CLAUDE.md
says (or does not say) about Board / Work; gwt's coordination
contract supersedes project-local documentation.

## Board (coordination history)

When you cross a reasoning milestone (phase change, alternative chosen,
or concern raised) or a coordination boundary (claim, next, handoff,
blocked, or decision), post to the shared Board.
Do not post tool-level updates such as "running gcc", "opening file X",
or "ran test Y". Do not repeat information already obvious from a diff
or command log.

Choose the audience before posting. If the post needs a reply, include
an explicit mention:

- `--mention user:<id>` - ask a human user for a response
- `--mention agent:<id>` - hand off to or question a specific agent type
- `--mention session:<id>` - reply to a running session
- `--mention branch:<name>` - target a workspace branch audience
- `--broadcast` - make the post visible repo-wide/cross-workspace

Without `--broadcast`, `gwtd board post` keeps the post scoped to the
current workspace and any audience implied by mentions. Do not assume
that omitting mentions creates a broadcast post.

Typical `--body` shape:

    Current state: <what changed or what was learned>
    Reason: <why it matters or why this decision was chosen>
    Next: <what happens next; omit when there is no next action>

Primary kinds:

- `--kind status` - phase change or hypothesis validation
- `--kind decision` - confirmed decision
- `--kind claim` - ownership claim to prevent collisions
- `--kind next` - next work broadcast
- `--kind blocked` - unblock request
- `--kind handoff` - concrete handoff

## Work (current state)

The Board is history; Work is current state. Update Work when
the current task, summary, next action, or focus changes:

    gwtd workspace update --agent-session "$GWT_SESSION_ID" \
      --current-focus '<current work>' \
      --title-summary '<short task title>'

`--title-summary` is the short task name used by Agent window tabs and
Work summaries. Write the work purpose, not its completion state, and
never copy the raw prompt into it. On a new Agent window, set it as your
first action, before responding to the user. If the purpose is not settled
yet, set a plausible provisional purpose now and update the same
`--title-summary` the moment the purpose is confirmed.

- Good: `Agent title summaries`
- Bad (completion state): `Agent title summaries done`, `Working on agent title summaries`
- Bad (activity / phase descriptors): `PR check in progress`, `verifying tests`, `fixing bug`, `checking CI`, `reviewing PR`

Rule: title-summary = work scope; phase / activity descriptors are for
`--current-focus` or Board `--body`. Keep the title stable while the
phase or activity changes, and update it only when the underlying work
scope changes (for example, starting a new task or moving to a
different Issue / SPEC).

Put completion, active, blocked, and similar state in `--status`,
`--current-focus`, `--summary`, or the Board `--body`, not in
`--title-summary`.

## Git environment

Do not manually create, switch, or delete branches/worktrees:

- Forbidden commands: `git checkout -b`, `git switch -c`,
  `git branch -D`, `git worktree add/remove`
- Git environment creation is owned by gwt Start Work / Launch
  materialization
- Existing branch switching, deletion, and renaming should happen only
  through human-driven operations

## Skills

`gwt-*` skill names (for example `gwt-search`, `gwt-discussion`) are
skills, never PATH executables. Do not resolve them with `command -v`
or `Get-Command`, and do not treat an empty PATH lookup as a missing
command. Invoke them through the runtime's skill mechanism, or follow
the executable commands written inside the skill's own SKILL.md.

## Persisted Work files

The tracked `.gwt/work/` directory is the persistent Work core
(SPEC-2359 W-15, FR-383). When you commit your work, include the
changed tracked files under `.gwt/work/` (`events.jsonl`, `memory.md`,
`discussions.md`) in the regular commit so they reach the base branch
through the PR:

- `.gwt/work/events.jsonl` is an append-only log joined across branches
  via the `merge=union` gitattribute. Never hand-edit, truncate, or
  revert it
- Do not commit it separately from the work it describes; ride the
  normal work commit

## Language

Write narrative Board bodies and Work summaries in Japanese.
Keep `gwtd` subcommands, flags, code examples, and technical identifiers
in English.

## Examples

    gwtd board post --kind claim --broadcast \
      --body $'現在の状態: migration slice を担当します。\n\nBoundary: 他 Agent は crates/gwt-core/src/migration.rs を避けてください。'

    gwtd board post --kind status --broadcast --title-summary '<short>' \
      --body $'現在の状態: focused tests が RED です。\n\n理由: CLI と hook output が multiline Board body を collapse しています。\n\n次: block rendering を実装します。'

    gwtd board post --kind handoff --mention agent:codex \
      --body $'現在の状態: phase 1 は local merge 済みです。\n\n次: Windows-focused verification を実行して failure を報告してください。'

    gwtd workspace update --agent-session "$GWT_SESSION_ID" \
      --current-focus 'SPEC-1935 Phase 2 writer' \
      --title-summary 'coordination guidance writer'
"#;

